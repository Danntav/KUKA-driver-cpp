RDK = Robolink()

#Robot object creation
robot = RDK.Item("KUKA LBR iiwa 14 R820", ITEM_TYPE_ROBOT)
if not robot.Valid():
    raise Exception('Could not find robot KUKA LBR iiwa 14 R820')

#Robot frame creation
robotFrame = robot.Parent()
if robotFrame.Type() != ITEM_TYPE_FRAME:
    raise Exception('Robot parent is not a frame')



