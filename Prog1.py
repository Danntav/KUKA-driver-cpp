def Prog1():
    # Create program item for: Prog1
    Prog1 = RDK.Item('Prog1',ITEM_TYPE_PROGRAM)
    if Prog1.Valid():
        raise Exception('Program with name Prog1 already exists.')
    Prog1 = RDK.AddProgram('Prog1')
    Prog1.setParam("Tree","collapse")

